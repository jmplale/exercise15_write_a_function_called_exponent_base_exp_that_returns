# write the given function from the exercise
def exponent(base, exp):
    # use a pow function
    outcome = pow(base, exp)
    # use a return statement
    return outcome

# set a base and an exponent
base_one = 2
exponent_one = 5

# print the result
print(f"{base_one} raises to the power of {exponent_one} is: {exponent(base_one, exponent_one)}")




